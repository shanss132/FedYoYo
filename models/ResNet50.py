def resnet20(num_classes):
    return ResNet(BasicBlock, [3, 3, 3], num_classes=num_classes)

def resnet32(num_classes):
    return ResNet(BasicBlock, [5, 5, 5], num_classes=num_classes)

def resnet18(num_classes,use_proj_head,proj_dim):
    return ResNetWider(BasicBlock, [2, 2, 2, 2], num_classes=num_classes, use_proj_head=use_proj_head, proj_dim=proj_dim)



def resnet50(num_cls):
    return ResNetWider(Bottleneck, [3, 4, 6, 3],num_classes=num_cls)


